var normalizeFrame = require("../lib/normalize-frame.js")
var test = require("tape")

test("normalize-frame", function(t){
  t.equal(0, normalizeFrame(0, 12).frame, "# 0 == 0")
  t.equal(0, normalizeFrame(.1, 12).frame, "# .1 == 0")
  t.equal(0, normalizeFrame(.9, 12).frame, "# .9 == 0")
  t.equal(1, normalizeFrame(1, 12).frame, "# 1 == 1")
  t.equal(1, normalizeFrame(1.5, 12).frame, "# 1.5 == 1")
  t.equal(9, normalizeFrame(9, 12).frame, "# 9 == 9")
  t.equal(0, normalizeFrame(12,12).frame, "# 12 == 0")
  t.equal(1, normalizeFrame(13, 12).frame, "# 13 == 1")
  t.equal(3, normalizeFrame(27, 12).frame, "# 27 == 3")
  t.equal(11, normalizeFrame(-1,12).frame, "# -1 == 11")
  t.equal(10, normalizeFrame(-2,12).frame, "# -2 == 10")
  t.equal(11, normalizeFrame(-.2,12).frame, "# -.2 == 11")
  t.equal(11.8, normalizeFrame(-.2,12).partialFrame, "# -.2 == 11.2")
  t.end()
})
